package learning.inheritance2;

public class MinorAccount extends SavingsAccount{
	
	
	
	private int age;
	
	private int grade;

	
	
	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getGrade() {
		return grade;
	}

	public void setGrade(int grade) {
		this.grade = grade;
	}
	
	

}
